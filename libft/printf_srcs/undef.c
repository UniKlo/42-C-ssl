#include "../include/printf.h"

void	undef(t_block *blk)
{
	if (blk->specifier == 'Z')
		write(1, "Z", 1);
	*blk->ret += 1;
}

int		bigger(int a, int b)
{
	if (a >= b)
		return (a);
	return (b);
}
